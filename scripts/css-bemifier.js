/**
 * UIverse - CSS BEM Converter
 * Converts monolithic CSS to BEM naming convention
 * 
 * Usage: node scripts/css-bemifier.js
 */

const fs = require('fs');
const path = require('path');

const INPUT_FILE = path.join(__dirname, '..', 'style.css');
const OUTPUT_FILE = path.join(__dirname, '..', 'style-bem.css');

const BEM_MAPPINGS = {
  'card': 'uiv-card',
  'btn': 'uiv-btn',
  'button': 'uiv-btn',
  'container': 'uiv-container',
  'wrapper': 'uiv-wrapper',
  'header': 'uiv-header',
  'footer': 'uiv-footer',
  'nav': 'uiv-nav',
  'sidebar': 'uiv-sidebar',
  'menu': 'uiv-menu',
  'input': 'uiv-input',
  'form': 'uiv-form',
  'label': 'uiv-label',
  'title': 'uiv-title',
  'text': 'uiv-text',
  'heading': 'uiv-heading',
  'section': 'uiv-section',
  'grid': 'uiv-grid',
  'row': 'uiv-row',
  'col': 'uiv-col',
  'modal': 'uiv-modal',
  'popup': 'uiv-popup',
  'dropdown': 'uiv-dropdown',
  'list': 'uiv-list',
  'item': 'uiv-item',
  'link': 'uiv-link',
  'icon': 'uiv-icon',
  'image': 'uiv-img',
  'table': 'uiv-table',
  'badge': 'uiv-badge',
  'alert': 'uiv-alert',
  'toast': 'uiv-toast',
  'loader': 'uiv-loader',
  'spinner': 'uiv-spinner'
};

function toBEMSelector(selector) {
  let result = selector;
  
  Object.entries(BEM_MAPPINGS).forEach(([old, bem]) => {
    const regex = new RegExp(`\\.${old}(?![a-zA-Z_-])`, 'g');
    result = result.replace(regex, `.${bem}`);
  });
  
  return result;
}

function convertToBEM(css) {
  let result = css;
  
  const blockRegex = /([.#][a-zA-Z_-][a-zA-Z0-9_-]*)\s*\{/g;
  result = result.replace(blockRegex, (match, selector) => {
    return `${toBEMSelector(selector)} {`;
  });
  
  result = result.replace(/\.([a-z]+)(__[a-z]+)/g, (match, block, elem) => {
    const bemBlock = BEM_MAPPINGS[block] || `uiv-${block}`;
    return `.${bemBlock}${elem}`;
  });
  
  result = result.replace(/([a-z]+)--([a-z-]+)/g, (match, block, mod) => {
    const bemBlock = BEM_MAPPINGS[block] || `uiv-${block}`;
    return `${bemBlock}--${mod}`;
  });
  
  return result;
}

function generateBEMGuide() {
  let guide = `/* UI-Verse BEM Naming Convention Guide */\n`;
  guide += `/* Generated by css-bemifier.js */\n\n`;
  guide += `/* Block: stand alone entity, meaningful on its own */\n`;
  guide += `.uiv-card { }\n\n`;
  guide += `/* Element: part of a block, no meaning alone */\n`;
  guide += `.uiv-card__header { }\n\n`;
  guide += `/* Modifier: change appearance or behavior */\n`;
  guide += `.uiv-card--featured { }\n\n`;
  guide += `/* Mappings from legacy to BEM: */\n`;
  Object.entries(BEM_MAPPINGS).forEach(([old, bem]) => {
    guide += `/* .${old} -> .${bem} */\n`;
  });
  return guide;
}

function main() {
  console.log('[BEMifier] Converting CSS to BEM naming convention...\n');

  if (!fs.existsSync(INPUT_FILE)) {
    console.error('[BEMifier] Input file not found:', INPUT_FILE);
    process.exit(1);
  }

  const originalCSS = fs.readFileSync(INPUT_FILE, 'utf8');
  console.log(`[BEMifier] Read ${originalCSS.split('\n').length} lines`);

  const bemCSS = convertToBEM(originalCSS);
  console.log(`[BEMifier] Converted to BEM`);

  const guide = generateBEMGuide();
  const finalOutput = guide + '\n' + bemCSS;

  fs.writeFileSync(OUTPUT_FILE, finalOutput);
  console.log(`[BEMifier] Written to: ${OUTPUT_FILE}`);
  console.log('[BEMifier] Done!');
}

main();