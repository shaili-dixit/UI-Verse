#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const dataDir = path.join(root, 'data');
const componentsFile = path.join(dataDir, 'components.json');
const metaDir = path.join(dataDir, 'meta');
const outDir = path.join(root, 'contributions');

const argv = process.argv.slice(2);
const flags = new Set(argv.filter(a => a.startsWith('--')));
const compArgIndex = argv.findIndex(a => a === '--component');
const compId = compArgIndex >= 0 ? argv[compArgIndex+1] : null;
const generateAll = flags.has('--all');
const checkOnly = flags.has('--check');

function loadJson(file) { return JSON.parse(fs.readFileSync(file, 'utf8')); }
function write(file, content) { fs.mkdirSync(path.dirname(file), { recursive: true }); fs.writeFileSync(file, content, 'utf8'); }

function renderTemplate(component, meta) {
  const title = component.title || meta && meta.title || component.id;
  const description = component.description || (meta && meta.description) || '';
  const tags = (component.tags || []).join(', ');
  const pathRef = component.path || (meta && meta.path) || `${component.id}.html`;
  const styles = meta && meta.dependencies && meta.dependencies.styles ? meta.dependencies.styles.join('\n') : '';
  const scripts = meta && meta.dependencies && meta.dependencies.scripts ? meta.dependencies.scripts.join('\n') : '';
  const changelogLink = `data/meta/${component.id}.json`;

  return `# Component Contribution Template: ${title}

> Component id: ${component.id}

## Summary

${description}

**Category:** ${component.category || ''}

**Tags:** ${tags}

**Path / Preview:** ${pathRef}

## Example Markup

<!-- Add minimal example markup for the component -->
<div class="component-${component.id}">
  <!-- ... -->
</div>

## Styles

${styles ? 'Include these styles or references:\n' + styles : 'No style references declared.'}

## Scripts

${scripts ? 'Include these scripts or references:\n' + scripts : 'No script references declared.'}

## Accessibility Checklist

- [ ] Keyboard operable
- [ ] Focus visible
- [ ] ARIA roles/labels where required
- [ ] Color contrast (AA)

## Changelog / Metadata

See metadata: ${changelogLink}

## Contribution Notes

1. Add usage examples and variants.
2. Provide tests and visual snapshots when applicable.
3. Document accessibility concerns and remediation steps.

---
Generated by scripts/generate-component-contribution.js
date: ${new Date().toISOString()}
component: ${component.id}
source: ${pathRef}
meta: ${meta ? changelogLink : 'none'}
file: contributions/${component.id}-CONTRIBUTION.md
`
}

function generateFor(component) {
  const metaFile = path.join(metaDir, `${component.id}.json`);
  const meta = fs.existsSync(metaFile) ? loadJson(metaFile) : null;
  const content = renderTemplate(component, meta);
  const outFile = path.join(outDir, `${component.id}-CONTRIBUTION.md`);
  if (checkOnly) {
    if (!fs.existsSync(outFile)) return { id: component.id, ok: false, reason: 'missing' };
    const existing = fs.readFileSync(outFile, 'utf8');
    return { id: component.id, ok: existing === content };
  }
  write(outFile, content);
  return { id: component.id, ok: true, file: outFile };
}

function main() {
  if (!fs.existsSync(componentsFile)) {
    console.error('components.json not found'); process.exit(2);
  }
  const components = loadJson(componentsFile);
  const toGen = generateAll ? components : (compId ? components.filter(c => c.id === compId) : []);
  if (!generateAll && !compId) {
    console.error('Specify --component <id> or --all'); process.exit(2);
  }

  const results = toGen.map(generateFor);
  const failed = results.filter(r => !r.ok);
  if (checkOnly && failed.length) {
    console.error('Contribution templates out of date or missing for:', failed.map(f => f.id).join(', '));
    process.exit(1);
  }

  results.forEach(r => { if (r.file) console.log('Wrote', r.file); });
  console.log('Done.');
}

if (require.main === module) main();
