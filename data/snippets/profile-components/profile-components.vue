    document.querySelectorAll('.upload-card').forEach(card => {
      card.style.display = (cat === 'all' || card.dataset.cat === cat) ? '' : 'none';
    });
  }

  /* ---------- Live Search ---------- */
  function liveFilter(val) {
    const q = val.toLowerCase();
    document.querySelectorAll('.upload-card').forEach(card => {
      const name = card.dataset.name || '';
      const textNode = card.querySelector('.card-top h3')?.textContent || '';
      const match = name.toLowerCase().includes(q) || textNode.toLowerCase().includes(q);
      card.style.display = match ? '' : 'none';
    });
  }

  /* ---------- Expandable Card Tab Switching ---------- */
  function switchTab(btn, paneName) {
    const card = btn.closest('.expandable-card');
    
    // Deactivate current tabs and panes
    card.querySelectorAll('.tab-btn').forEach(b => b.classList.remove('active'));
    card.querySelectorAll('.tab-pane').forEach(p => p.classList.remove('active'));
    
    // Activate clicked tab
    btn.classList.add('active');
    
    // Activate matching pane
    const targetPane = card.querySelector(`.tab-pane[data-pane="${paneName}"]`);
    if (targetPane) {
      targetPane.classList.add('active');
    }
  }
</script>
